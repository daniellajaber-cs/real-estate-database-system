Property_ID INT NOT NULL PRIMARY KEY ,
Address VARCHAR(100) NOT NULL,
Size_SQM INT NOT NULL CHECK(Size_SQM > 0),
Bedrooms INT NOT NULL CHECK(Bedrooms >0),
Bathrooms INT NOT NULL CHECK(Bathrooms >=0),
Base_Price INT NOT NULL CHECK(Base_Price >0),
Status VARCHAR(20) NOT NULL DEFAULT 'Available ' CHECK( Status IN ('Available','UnderOffer','Sold')),
Owner_ID INT NOT NULL ,
Location_ID INT NOT NULL ,
Type_ID INT NOT NULL,
FOREIGN KEY (Owner_ID ) REFERENCES Owner(Owner_ID) ON DELETE CASCADE ON UPDATE CASCADE,
FOREIGN KEY (Location_ID ) REFERENCES Location(Location_ID) ON DELETE RESTRICT ON UPDATE CASCADE,
FOREIGN KEY (Type_ID ) REFERENCES PropertyType(Type_ID) ON DELETE RESTRICT ON UPDATE CASCADE
